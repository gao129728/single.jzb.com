                <h3 class="ztit">SEO 第三步：网站内容发布规范</h3>
                <p class="blue">· 如何设置标题</p>
                <p>不管是百度还是google，信息标题都将做为其衡量页面排名的最最之重。</p>
                <p>如何选一个好的标题有三点：</p>
                <p>a、标题中一定要含有关键词，关键词最多不要超过两个。</p>
                <p>b、重要的关键词要放到标题的最前面。</p>
                <p>c、多个关键词之间用 空格或_ 分开，而不要采用其他特殊符号。</p>
                <p class="blue">· 如何发布内容</p>
                <p>a、内容一定要与标题相关。</p>
                <p>一个内容与标题不相关的页面，搜索引擎将会做为垃圾页面处理。所以我们发布的内容一定要与标题相关。内容中要含有相应的关键词。不易过多，重复3-5次最好。</p>
                <p>b、不要在多个网站发布相同内容的信息</p>
                <p>当我们发布信息时，往往会发布到多个网站上。这就带来一个问题，搜索引擎将只会看重其中的一个页面。其他的页面将做为垃圾页面处理。等于我们在浪费时间做没用的事。 我们可以把内容适当做一些修改。修改标题，内容适当增加和缩减。使他们看起来更像无重复的页面。</p>
                <p>c、选一个好的网站做为信息的长久发布平台</p>
                <p>选一个页面整洁，布局清晰的网站做为企业信息的发布平台，这样不仅利于信息的长久维护。而且更能吸引我们的客户浏览。 而且也会影响到企业的形象。</p>
                <h3 class="ztit">SEO 第四步：给网站加外部链接</h3>
                <p class="blue">· 什么叫外部链接？</p>
                <p>外部链接指的是针对搜索引擎友情链接，高质量的外部链接指和您的网站建立链接的网站知名度高，访问量大，同时相对的外部链接较少，有助与快速提升您的网站知名度和排名。</p>
                <p class="blue">· 如何加外部链接？</p>
                <p>如何增加外链对一个网站来讲非常的重要，可以提高网站的排序,获得好的排名；也可以带来不错的流量。</p>
                <p>下面是如何增加外链的一些方法和技巧：</p>
                <p>方法一：做友情连接,如果是单向连接那更好，就是别人的网站做你的链接而您不用做别人的链接.这样效果最好。</p>
                <p>方法二：写一些高质量的软文发布到各大网站，如果大家认可，会有无数的网站转载，这样可以获得很多好的外链。</p>
                <p>方法三：在Google和Baidu查询您网站的主题.看看排名第一页的网站的反向链接。然后联系这些跟排名前面做的链接的网站跟您交换链接。</p>
                <p>方法四：如果您经常泡论坛.把论坛的个性签名改成您网站的关键词链接.把你的网站地址，放到你签名里面,到百度贴吧和百度知道里面适当的发下广告。</p>
                <p>方法五：现在流行ask爱问、Baidu知道等问答系统，在这些站点搜索您网站的关键词，回答相关的问题关留下您网站网址。</p>
                <p>方法六：多去别人的博客上留言，特别是与你博客主题相关的，现在的一些博客系统都支持带链接的留言了。这种获得的链接又快又好。</p>
