                <h3 class="ztit">SEO 第一步：选择关键词</h3>
                <p class="blue">· 如何选择关键词？</p>
                <p>关键词的选择将直接决定您网站的优化效果，所以要对您的关键词做必要的前期分析，挑选去最适合的关键词以达到最佳的优化效果。</p>
                <p>总结了以下几点关键词的选择标准：</p>
                <p>a、关键词要与公司的业务相关，并选择百度中竞价较少的关键词。</p>
                <p>b、关键词不要太宽泛，比如做房地产的公司，想针对房地产这个词优化，基本效果是很难达到的。</p>
                <p>c、可以选择 “地区+业务关键词”的组合来做为优化的关键词。</p>
                <p>d、关键词也不适于太长太特殊。</p>
                <p>e、站在用户角度思考，对您的客户做个简单调查，了解他们常用的关键词。</p>
                <p>f、要发布关键词相关的服务或产品信息，来做为优化页面。</p>
